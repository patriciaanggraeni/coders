package course.oop.`class`

/**
 * Class merupakan sebuah blueprint objek. Di dalam class terdapat properti (field) dan fungsi (method/behaviour). Seperti
 * di Java, di Kotlin cara membuat sebuah kelas cukup menggunakan kata kunci class. Class inilah yang nantinya akan di
 * jadikan sebuah objek. Peraturan penamaan class menggunakan camelCase/kapital setiap kata dan sangat disarankan untuk
 * memberi nama class sama dengan nama file seperti di Java. Kotlin berbeda dengan Java yang mana setiap file hanya dapat
 * memiliki satu class, di Kotlin setiap file bisa memiliki banyak class.
 */

// membuat sebuah class
private class Employee
private class Car

fun main() {

    println("Kelas Employee: ${Employee()}")
    println("Kelas Employee: ${Car()}")

}