package Course.B_DataStructures.DataStructures.A_Array;

public class a_introduction {
    public static void main(String[] args) {

        // deklarasi array di java
        int[] arr1; // disarankan
        int arr2[]; // tidak disarankan (c/c++ style)

        // inisialisasi array ke memory
        arr1 = new int[5];
        arr2 = new int[5];

        // instansiasi array (deklarasi dan inisialisasi)
        int[] arr3 = new int[5];
        int[] arr4 = {1,2,3,4,5};


        /*
            - index array mulai dari nol (default)
            - index terakhir adalah length - 1 (5 - 1 = 4 (index terakhir))
        */
    }
}
