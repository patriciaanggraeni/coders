package Course.A_Basic.Operator_05;

public class Komparison {
    public static void main(String[] args) {

        // Operator perbandingan menghasilkan nilai boolean
        int x = 13;
        int y = 10;
        boolean sum1 = x > y;
        boolean sum2 = x < y;

        System.out.printf("Hasil %d > %d = %b", x, y, sum1);
        System.out.printf("\nHasil %d < %d = %b", x, y, sum2);
        System.out.println("\n");
    }
}
