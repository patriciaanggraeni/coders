package Course.A_Basic.Operator_05;

public class Aritmathics {
    public static void main(String[] args) {

        /*

        Operator di java :

        1. +, Penjumlahan
        2. -, Pengurangan
        3. *, Perkalian
        4. /, Pembagian
        5. %, Modulus (sisa bagi)
        6. a++, ++a, Increment Pre / Post Increment
        7. a--, --a, Decrement Pre / Post Increment
        8. <, >, <=, >=, ==, !=, Perbandingan
        9. &&, ||, !, Logika
        10. &, |, ^, ~, <<, >>, Bitwise
        11. =, +=, -=, *=, /=, %=, Assigment

        */

        // Pemjumlahan (Berlaku operator aritmatika lainnya)
        int a = 15;
        int b = 15;
        int hasil = a + b;
        System.out.printf("\nHasil %d + %d = %d", a, b, hasil);
        System.out.println("\n");
    }
}
